#include "3-calc.h"
/**
 * op_add - adds
 * @a: num1
 * @b: num2
 * Return: ans
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - minus
 * @a: num1
 * @b: num2
 * Return: ans
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies
 * @a :num1
 * @b: num2
 * Return: ans
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: num1
 * @b: num2
 * Return: ans
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder
 * @a: num1
 * @b: num2
 * Return: ans
 */
int op_mod(int a, int b)
{
	return (a % b);
}
